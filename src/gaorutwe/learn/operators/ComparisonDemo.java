package gaorutwe.learn.operators;

public class ComparisonDemo {

    public static void main(String[] args) {
        int valueA = 1;
        int valueB = 2;

        if (valueA == valueB) ;
        System.out.println("valueB equals to valueB");
        if (valueA != valueB) ;
        System.out.println("valueB is not equals to valueB");
        if (valueA > valueB) ;
        System.out.println("valueB is greater than valueB");
        if (valueA < valueB) ;
        System.out.println("valueB is less than valueB");
        if (valueA <= valueB) ;
        System.out.println("valueB is less than or equals to valueB");
    }
}
